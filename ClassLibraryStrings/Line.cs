namespace ClassLibraryStrings
{
    public class Line
    {
        public static void LineOneHyphen()
        {
            Console.WriteLine("------------------------------------------------------" +
                              "-----------------------------------------------------------------");
        }

        public static void LineTwoEqual()
        {
            Console.WriteLine("======================================================" +
                              "==================================================================" +
                              Environment.NewLine);
        }

        
        public static void LineThreeStar()
        {
            Console.WriteLine("******************************************************" +
                              "******************************************************************" +
                              Environment.NewLine);
        }
    }
}